package com.lec.spring1;

public class Calculator {
public void addC(int f,int s) {
	
	int result = f+s;
	System.out.println("first + second"+ result);
}
public void subC(int f,int s) {
	
	int result = f-s;
	System.out.println("first + second"+ result);
}
public void mulC(int f,int s) {
	
	int result = f*s;
	System.out.println("first + second"+ result);
}
public void divC(int f,int s) {
	
	int result = f/s;
	System.out.println("first + second"+ result);
}
}
